/*
La fonction ft_strnstr recherche dans les n premiers octets de la chaîne de 
caractères donnée s1 pour la première occurrence de la chaîne complète s2.
Les caractères qui apparaissent après \0 ne sont pas recherchés.
*/

#include "../includes/libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (len == 0 && big == 0)
		return (0);
	if (little[i] == 0)
		return ((char *)big);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && (i + j) < len)
		{
			j++;
			if (little [j] == '\0')
				return ((char *)&big[i]);
		}
		i++;
	}
	return (0);
}
